package com.premiumminds.protocols.ftp.api;

public class FtpClientRuntimeException extends RuntimeException {
	private static final long serialVersionUID = 1L;

	public FtpClientRuntimeException() {
		super();
		return;
	}

	public FtpClientRuntimeException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		return;
	}

	public FtpClientRuntimeException(String message, Throwable cause) {
		super(message, cause);
		return;
	}

	public FtpClientRuntimeException(String message) {
		super(message);
		return;
	}

	public FtpClientRuntimeException(Throwable cause) {
		super(cause);
		return;
	}

	

}
